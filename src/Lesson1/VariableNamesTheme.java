package lesson1;

public class VariableNamesTheme {
    public static void main(String[] args) {
        System.out.println("1. Various variables\n");

        // Цифра
        int digit = 1;
        System.out.println("Цифра: " + digit);
        // Сумма чисел
        int sumDigits = 3;
        System.out.println("Сумма чисел: " + sumDigits);
        // Произведение цифр
        int prodDigits = 2;
        System.out.println("Произведение цифр: " + prodDigits);
        // Максимальное число
        int maxNumber = 100;
        System.out.println("Максимальное число: " + maxNumber);
        // Количество десятков
        int tensNumber = 2;
        System.out.println("Количество десятков: " + tensNumber);
        // Вес собаки
        float dogsWeight = 3.65f;
        System.out.println("Вес собаки: " + dogsWeight + " кг");
        // Процент по вкладу
        float depositPercent = 20.5f;
        System.out.println("Процент по вкладу: " + depositPercent + "%");
        // Переменная хранит символ &
        char ampersand = '&';
        System.out.println("Переменная хранит символ: " + ampersand);
        // Код ошибки
        int errorCode = 404;
        System.out.println("Код ошибки: " + errorCode);
        // Тип сообщения
        String messageType = "Information";
        System.out.println("Тип сообщения: " + messageType);
        // Число нулей
        short zerosNumber = 6;
        System.out.println("Число нулей: " + zerosNumber);
        // Уникальное число
        int uniqueNumber = 777;
        System.out.println("Уникальное число: " + uniqueNumber);
        // Случайное число
        int randomNumber = 9123;
        System.out.println("Случайное число: " + randomNumber);
        // Математическое выражение
        String mathExpression = "2x - 1 = 0";
        System.out.println("Математическое выражение: " + mathExpression);
        // Счет в игре
        String gameScore = "2 : 1";
        System.out.println("Счет в игре: " + gameScore);
        // Максимальная длина
        int maxLength = 17;
        System.out.println("Максимальная длина: " + maxLength);
        // Пункт в меню
        String menuItem = "Mushroom soup";
        System.out.println("Пункт в меню: " + menuItem);
        // Стоимость кофе
        float coffeePrice = 2.99f;
        System.out.println("Стоимость кофе: " + coffeePrice + " рублей");
        // Дата начала
        String startDate = "01.10.2024";
        System.out.println("Дата начала: " + startDate);
        // Окончание диапазона
        String endDate = "31.01.2025";
        System.out.println("Окончание диапазона: " + endDate);
        // Полное имя работника месяца
        String fullEmployeeOfTheMonthName = "Иванов Иван Иванович";
        System.out.println("Полное имя работника месяца: " + fullEmployeeOfTheMonthName);
        // Заголовок электронной книги
        String ebookTitle = "1.Предисловие";
        System.out.println("Заголовок электронной книги: " + ebookTitle);
        // Размер
        String size = "Large";
        System.out.println("Размер: " + size);
        // Вместимость
        int capacity = 250;
        System.out.println("Вместимость: " + capacity);
        // Счетчик
        int counter = 19513;
        System.out.println("Счетчик: " + counter);
        // Путь до файла
        String path = "C:/Users/folder/readme.txt";
        System.out.println("Путь до файла: " + path);
        // Количество чисел в каждой строке
        int numCountInLine = 4;
        System.out.println("Количество чисел в каждой строке: " + numCountInLine);

        System.out.println("\n2. Boolean variables\n");

        // Сотни равны?
        boolean isEqualHundreds = true;
        System.out.println("Сотни равны?: " + isEqualHundreds);
        // Компьютер включен?
        boolean isComputerPoweredOn = false;
        System.out.println("Компьютер включен?: " + isComputerPoweredOn);
        // Есть равные цифры?
        boolean hasEqualDigits = false;
        System.out.println("Есть равные цифры?: " + hasEqualDigits);
        // Создано?
        boolean isCreated = true;
        System.out.println("Создано?: " + isCreated);
        // Пустое?
        boolean isEmpty = false;
        System.out.println("Пустое?: " + isEmpty);
        // Активное?
        boolean isActive = false;
        System.out.println("Активное?: " + isActive);
        // Новое?
        boolean isNew = true;
        System.out.println("Новое?: " + isNew);
        // Электронная почта действительна?
        boolean isValidEmail = true;
        System.out.println("Электронная почта действительна?: " + isValidEmail);
        // Имеются уникальные строки?
        boolean hasUniqueLines = true;
        System.out.println("Имеются уникальные строки?: " + hasUniqueLines);

        System.out.println("\n2. Abbreviations variables\n");

        // Старый universally unique identifier
        String oldUuid = "42gh4n153gn59bl513";
        System.out.println("Старый universally unique identifier: " + oldUuid);
        // Производитель оперативной памяти
        String ramManufacturer = "Gigabyte";
        System.out.println("Производитель оперативной памяти: " + ramManufacturer);
        // Емкость жесткого диска (в байтах)
        int hddCapacity = 1099511627;
        System.out.println("Емкость жесткого диска: " + hddCapacity + " байт");
        // Протокол передачи гипертекста
        String httpProtocol = "HTTP/2";
        System.out.println("Протокол передачи гипертекста: " + httpProtocol);
        // Сокращенный uniform resource locator
        String url = "https://google.com";
        System.out.println("Сокращенный uniform resource locator: " + url);
        // Новый идентификатор клиента
        int newClientId = 424;
        System.out.println("Новый идентификатор клиента: " + newClientId);
        // American standard code for information interchange
        int asciiCode = 51;
        System.out.println("American standard code for information interchange: " + asciiCode);
    }
}
